# Benefits of Object Oriented Programming:

# 1. Easier to solve complex problems using real world nouns and verbs.
# 2. Hiding pieces of functionality makes it easier to parse and maintain code.
# 3. Helps to break down complex problems into chunks of functionality.
# 4. Easier to debug large programs.
# 5. Reduced duplication when writing code.

# ==========

# Given answer:

# 1. Creating objects allows programmers to think more abstractly about the code
# they are writing.
# 2. Objects are represented by nouns so are easier to conceptualize.
# 3. It allows us to only expose functionality to the parts of code that need
# it, meaning namespace issues are much harder to come across.
# 4. It allows us to easily give functionality to different parts of an
# application without duplication.
# 5. We can build applications faster as we can reuse pre-written code.
# 6. As the software becomes more complex this complexity can be more easily
# managed.
